  document.addEventListener("DOMContentLoaded", () => {
    const categoryButtonsContainer = document.getElementById("categoryButtons");
    const examCategoriesContainer = document.getElementById("examCategories");
    const skillCategoriesContainer = document.getElementById("skillCategories");
    const subCategoryContainer = document.getElementById("subCategoryContainer");
    const examSubCategoriesContainer =
      document.getElementById("examSubCategories");
    const skillSubCategoriesContainer =
      document.getElementById("skillSubCategories");
    const roadmapFlowContainer = document.getElementById("roadmapFlow");
  
    function createCategoryButton(category) {
      const button = document.createElement("button");
      button.textContent = category;
      button.className = "category";
      button.addEventListener("click", () => {
        displayCategories(category);
      });
      categoryButtonsContainer.appendChild(button);
    }
  
    function displayCategories(category) {
      // Clear previous content from containers
      examCategoriesContainer.innerHTML = "";
      skillCategoriesContainer.innerHTML = "";
      examSubCategoriesContainer.innerHTML = "";
      skillSubCategoriesContainer.innerHTML = "";
      roadmapFlowContainer.classList.add("d-none");
      subCategoryContainer.classList.add("d-none");
  
      // Remove 'active' class from all buttons
      const buttons = categoryButtonsContainer.querySelectorAll("button");
      buttons.forEach((btn) => btn.classList.remove("active"));
  
      // Add 'active' class to the clicked button
      const activeButton = Array.from(buttons).find(
        (btn) => btn.textContent === category
      );
      if (activeButton) activeButton.classList.add("active");
  
      // Display relevant sub-categories
      const categories = roadmapData[category];
      for (const [subCategory, items] of Object.entries(categories)) {
        const subCategoryDiv = document.createElement("div");
        subCategoryDiv.className = "subcategory";
        subCategoryDiv.textContent = subCategory;
        subCategoryDiv.addEventListener("click", () => {
          displaySubCategories(items, subCategory, category);
        });
        if (category === "Exams") {
          examCategoriesContainer.appendChild(subCategoryDiv);
        } else if (category === "Skills") {
          skillCategoriesContainer.appendChild(subCategoryDiv);
        }
      }
  
      // Show sub-category container
      subCategoryContainer.classList.remove("d-none");
    }
  
    function displaySubCategories(items, subCategory, category) {
      // Clear previous items
      roadmapFlowContainer.classList.add("d-none");
  
      // Clear previous content from sub-category containers
      examSubCategoriesContainer.innerHTML = "";
      skillSubCategoriesContainer.innerHTML = "";
  
      // Remove 'active' class from all subcategories
      const subcategories = document.querySelectorAll(".subcategory");
      subcategories.forEach((sub) => sub.classList.remove("active"));
  
      // Add 'active' class to the clicked subcategory
      const activeSubcategory = Array.from(subcategories).find(
        (sub) => sub.textContent === subCategory
      );
      if (activeSubcategory) activeSubcategory.classList.add("active");
  
      // Display subcategory items
      items.forEach((item) => {
        const itemDiv = document.createElement("div");
        itemDiv.className = "item";
        itemDiv.textContent = item.name;
        itemDiv.addEventListener("click", () => {
          displayRoadmap(item);
          // Remove 'active' class from all items
          document
            .querySelectorAll(".item")
            .forEach((i) => i.classList.remove("active"));
          // Add 'active' class to the clicked item
          itemDiv.classList.add("active");
        });
        if (category === "Exams") {
          examSubCategoriesContainer.appendChild(itemDiv);
        } else if (category === "Skills") {
          skillSubCategoriesContainer.appendChild(itemDiv);
        }
      });
  
      // Show subcategories
      if (category === "Exams") {
        examSubCategoriesContainer.classList.remove("d-none");
      } else if (category === "Skills") {
        skillSubCategoriesContainer.classList.remove("d-none");
      }
    }
  
    function displayRoadmap(item) {
      // Clear and show roadmapFlowContainer
      roadmapFlowContainer.innerHTML = `<h4>${item.name} - Roadmap</h4>`;
      roadmapFlowContainer.classList.remove("d-none");
  
      const roadmapKey = Object.keys(item.details).find((key) =>
        key.includes("Roadmap")
      );
      if (roadmapKey) {
        item.details[roadmapKey].forEach((step, index) => {
          const stepDiv = document.createElement("div");
          stepDiv.className = "roadmap-step";
          stepDiv.innerHTML = `<h5>Step ${index + 1}</h5><p>${step}</p>`;
          roadmapFlowContainer.appendChild(stepDiv);
        });
      }
    }
  
    // Create buttons for each category
    for (const category of Object.keys(roadmapData)) {
      createCategoryButton(category);
    }
  });